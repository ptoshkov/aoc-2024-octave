sum1 = 0;

for ii = 1:numel(obstacleLocations)
    ii
    location = obstacleLocations(ii);
    newData = data;
    newData(location) = '.';
    costMat = dijkstra(newData, startPos);
    newTime = costMat(endPos);
    timeDifference = defaultTime - newTime;

    if(timeDifference >= 100)
        sum1++;
    end
end

